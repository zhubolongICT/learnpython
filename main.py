# Python 变量和基础算术运算示例
# 变量赋值
a = 10  # 整数变量
b = 3.5  # 浮点数变量
c = "Hello"  # 字符串变量

# 基础算术运算
add = a + b  # 加法
sub = a - b  # 减法
mul = a * b  # 乘法
div = a / b  # 除法（结果为浮点数）
mod = a % 3  # 取余（模运算）
exp = a ** 2  # 幂运算（a的2次方）
floor_div = a // 3  # 整除（向下取整）

# 输出结果
print("a =", a)
print("b =", b)
print("c =", c)
print("a + b =", add)
print("a - b =", sub)
print("a * b =", mul)
print("a / b =", div)
print("a % 3 =", mod)
print("a ** 2 =", exp)
print("a // 3 =", floor_div)
# 一个简单的 Hello World 程序
print("Hello, World!")


